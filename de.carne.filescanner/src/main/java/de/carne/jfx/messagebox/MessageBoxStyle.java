package de.carne.jfx.messagebox;

/**
 * Message box styles.
 */
public enum MessageBoxStyle {

	/**
	 * Show info icon.
	 */
	ICON_INFO,

	/**
	 * Show warning icon.
	 */
	ICON_WARNING,

	/**
	 * Show error icon.
	 */
	ICON_ERROR,

	/**
	 * Show question icon.
	 */
	ICON_QUESTION,

	/**
	 * Show Ok button.
	 */
	BUTTON_OK,

	/**
	 * Show Ok and Cancel button.
	 */
	BUTTON_OK_CANCEL,

	/**
	 * Show Yes and No button.
	 */
	BUTTON_YES_NO,

	/**
	 * Show Yes, No and Cancel button.
	 */
	BUTTON_YES_NO_CANCEL

}
